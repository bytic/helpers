    public function __construct()
    {
        foreach ($this->colors as $key => $color) {
            $this->colors[$key] = array_merge($this->colors[$key], $this->rgb('#'.$color[0]));
            $this->colors[$key] = array_merge($this->colors[$key], $this->hsl('#'.$color[0]));
        }
    }


    public function rgb($color)
    {
        $r = substr($color, 1, 2);
        $g = substr($color, 3, 2);
        $b = substr($color, 5, 2);

        $r = hexdec($r);
        $g = hexdec($g);
        $b = hexdec($b);

        return array($r, $g, $b);
    }


    /**
     * @param string $color
     */
    public function hsl($color)
    {
        list($r, $g, $b) = $this->rgb($color);

        $r /= 255;
        $g /= 255;
        $b /= 255;

        $min = min(array($r, min(array($g, $b))));
        $max = max(array($r, max(array($g, $b))));

        $delta = $max - $min;
        $l = ($min + $max) / 2;

        $s = 0;
        if ($l > 0 && $l < 1) {
            $s = $delta / ($l < 0.5 ? (2 * $l) : 2 - (2 * $l));
        }

        $h = 0;
        if ($delta > 0) {
            if ($max == $r && $max != $g) {
                $h += ($g - $b) / $delta;
            }
            if ($max == $g && $max != $b) {
                $h += (2 + ($b - $r) / $delta);
            }
            if ($max == $b && $max != $r) {
                $h += (4 + ($r - $g) / $delta);
            }

            $h /= 6;
        }

        $h *= 255;
        $s *= 255;
        $l *= 255;

        return array(floor($h), floor($s), floor($l));
    }


    public function name($color)
    {
        $color = strtoupper($color);

        if (strlen($color) < 3 || strlen($color) > 7) {
            return false;
        }

        if (strlen($color) % 3 == 0) {
            $color = '#'.$color;
        }

        if (strlen($color) == 4) {
            $color = '#'.$color[1].$color[1].$color[2].$color[2].$color[3].$color[3];
        }

        list($r, $g, $b) = $this->rgb($color);
        list($h, $s, $l) = $this->hsl($color);

        $ndf1 = 0;
        $ndf2 = 0;
        $ndf = 0;
        $cl = -1;
        $df = -1;

        $count = count($this->colors);

        for ($i = 0; $i < $count; $i++) {
            if ($color == '#'.$this->colors[$i][0]) {
                return array('#'.$this->colors[$i][0], $this->colors[$i][1], true);
            }

            $ndf1 = pow($r - $this->colors[$i][2], 2) + pow($g - $this->colors[$i][3],
                    2) + pow($b - $this->colors[$i][4], 2);
            $ndf2 = abs(pow($h - $this->colors[$i][5], 2)) + pow($s - $this->colors[$i][6],
                    2) + abs(pow($l - $this->colors[$i][7], 2));

            $ndf = $ndf1 + $ndf2 * 2;
            if ($df < 0 || $df > $ndf) {
                $df = $ndf;
                $cl = $i;
            }
        }

        return $cl < 0 ? false : array("#".$this->colors[$cl][0], $this->colors[$cl][1], false);
    }


    /**
     * Uses luminosity to calculate the difference between the given colors.
     * The returned value should be bigger than 5 for best readability.
     *
     * @param string|array $color1
     * @param string|array $color2
     * @return double
     */
    public function lumDiff($color1, $color2)
    {
        list($R1, $G1, $B1) = is_array($color1) ? $color1 : $this->rgb($color1);
        list($R2, $G2, $B2) = is_array($color2) ? $color2 : $this->rgb($color2);

        $L1 = 0.2126 * pow($R1 / 255, 2.2) +
            0.7152 * pow($G1 / 255, 2.2) +
            0.0722 * pow($B1 / 255, 2.2);

        $L2 = 0.2126 * pow($R2 / 255, 2.2) +
            0.7152 * pow($G2 / 255, 2.2) +
            0.0722 * pow($B2 / 255, 2.2);

        if ($L1 > $L2) {
            return ($L1 + 0.05) / ($L2 + 0.05);
        } else {
            return ($L2 + 0.05) / ($L1 + 0.05);
        }
    }


    /**
     * Singleton
     *
     * @return Nip_Helper_Color
     */
    public static function instance()
    {
        static $instance;
        if (!($instance instanceof self)) {
            $instance = new self();
        }

        return $instance;
    }
